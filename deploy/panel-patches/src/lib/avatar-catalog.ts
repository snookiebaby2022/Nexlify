function hashPick<T>(seed: string, arr: readonly T[]): T {
  let h = 0;
  for (let i = 0; i < seed.length; i++) h = (h * 31 + seed.charCodeAt(i)) | 0;
  return arr[Math.abs(h) % arr.length];
}

/** Deterministic Reddit-style character from username when user has no saved avatar. */
export function avatarConfigFromUsername(username: string): AvatarConfig {
  const seed = username.trim().toLowerCase() || "user";
  return {
    ...DEFAULT_AVATAR_CONFIG,
    backgroundColor: hashPick(seed, BACKGROUND_COLORS),
    skinTone: hashPick(seed + "s", SKIN_TONES),
    hair: hashPick(seed + "h", HAIR_STYLES),
    hairColor: hashPick(seed + "hc", HAIR_COLORS),
    shirt: hashPick(seed + "sh", SHIRT_STYLES),
    shirtColor: hashPick(seed + "sc", CLOTHING_COLORS),
    pants: hashPick(seed + "p", PANTS_STYLES),
    pantsColor: hashPick(seed + "pc", CLOTHING_COLORS),
    hat: hashPick(seed + "hat", HAT_STYLES),
    hatColor: hashPick(seed + "hatc", CLOTHING_COLORS),
    eyes: hashPick(seed + "e", EYE_STYLES),
    mouth: hashPick(seed + "m", MOUTH_STYLES),
    blush: true,
  };
}

export function randomAvatarConfig(): AvatarConfig {
  const preset = AVATAR_PRESETS[Math.floor(Math.random() * AVATAR_PRESETS.length)];
  return { ...preset.config };
}

