function preset(label: string, patch: Partial<AvatarConfig>): AvatarPreset {
  const id = label.toLowerCase().replace(/\s+/g, "-");
  return {
    id,
    label,
    config: { ...DEFAULT_AVATAR_CONFIG, ...patch },
  };
}

