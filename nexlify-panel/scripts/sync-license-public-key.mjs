#!/usr/bin/env node
/**
 * Sync src/lib/license/public-key.ts from .license-keys/public.pem
 * Run after copying keys to this machine, or on VPS after license:setup.
 */
import fs from "fs";
import path from "path";

const root = process.cwd();
const pubPemPath = path.join(root, ".license-keys", "public.pem");
const pubOut = path.join(root, "src", "lib", "license", "public-key.ts");

if (!fs.existsSync(pubPemPath)) {
  console.error("Missing .license-keys/public.pem — run npm run license:setup first");
  process.exit(1);
}

const pubPem = fs.readFileSync(pubPemPath, "utf8").trim() + "\n";

const ts = `/**
 * AUTO-GENERATED by npm run license:sync-public-key — Ed25519 public key (safe to commit).
 * Private key: .license-keys/private.pem (never commit, never ship to customers).
 */
export const DEFAULT_LICENSE_PUBLIC_KEY_PEM = \`${pubPem}\`;

export function licensePublicKeyPem(): string {
  const fromEnv = process.env.NEXLIFY_LICENSE_PUBLIC_KEY?.trim();
  if (fromEnv) {
    return fromEnv.includes("BEGIN PUBLIC KEY")
      ? fromEnv
      : Buffer.from(fromEnv, "base64").toString("utf8");
  }
  return DEFAULT_LICENSE_PUBLIC_KEY_PEM;
}
`;

fs.writeFileSync(pubOut, ts);
console.log("Synced public key from", pubPemPath);
console.log("Updated", pubOut);
